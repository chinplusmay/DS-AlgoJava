public class NoValueArr {
    public static void main(String[] args){
        int[] defi = new int[4];
        String[] defs = new String[5];
// If we dont give values to array then BY default
        System.out.println(defi[0]);  // int type array will have 0 value
        System.out.println(defs[1]);  // String type array will have null value
    }
    
}
